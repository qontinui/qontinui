"""Cross-language fingerprint hash consistency tests.

Verifies that the Python FNV-1a hash implementation produces identical
hashes to the TypeScript implementation in element-fingerprint.ts.

These test vectors were generated by the TypeScript implementation.
If this test fails, the two implementations have diverged and recordings
will produce mismatched fingerprint hashes.
"""

import ctypes


def compute_hash_sync(
    structural_path: str,
    position_zone: str,
    role: str,
    accessible_name: str | None,
    size_category: str,
) -> str:
    """FNV-1a hash matching the TypeScript computeHashSync().

    Input format: "structuralPath|positionZone|role|accessibleName|sizeCategory"
    Output: 16-char hex string (two 32-bit FNV-1a hashes concatenated).
    """
    input_str = f"{structural_path}|{position_zone}|{role}|{accessible_name or ''}|{size_category}"

    h1 = 0x811C9DC5
    h2 = 0x811C9DC5

    for char in input_str:
        c = ord(char)
        # h1 ^= c; h1 = Math.imul(h1, 0x01000193)
        h1 ^= c
        h1 = ctypes.c_int32(h1 * 0x01000193).value
        # h2 ^= c * 31; h2 = Math.imul(h2, 0x01000193)
        h2 ^= ctypes.c_int32(c * 31).value
        h2 = ctypes.c_int32(h2 * 0x01000193).value

    # Convert to unsigned 32-bit and format as hex
    hex1 = format(ctypes.c_uint32(h1).value, "08x")
    hex2 = format(ctypes.c_uint32(h2).value, "08x")
    return hex1 + hex2


