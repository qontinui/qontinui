class TestFingerprintHashConsistency:
    """Verify Python hash matches TypeScript hash for the same inputs."""

    def test_produces_16_char_hex(self) -> None:
        for sp, pz, r, an, sc, _ in TEST_VECTORS:
            result = compute_hash_sync(sp, pz, r, an, sc)
            assert len(result) == 16, f"Expected 16 chars, got {len(result)}: {result}"
            assert all(c in "0123456789abcdef" for c in result)

    def test_all_hashes_unique(self) -> None:
        hashes = {compute_hash_sync(sp, pz, r, an, sc) for sp, pz, r, an, sc, _ in TEST_VECTORS}
        assert len(hashes) == len(TEST_VECTORS)

    def test_deterministic(self) -> None:
        for sp, pz, r, an, sc, _ in TEST_VECTORS:
            h1 = compute_hash_sync(sp, pz, r, an, sc)
            h2 = compute_hash_sync(sp, pz, r, an, sc)
            assert h1 == h2

    def test_matches_typescript_vectors(self) -> None:
        """THE critical test — Python must produce the same hash as TypeScript."""
        for sp, pz, r, an, sc, expected in TEST_VECTORS:
            actual = compute_hash_sync(sp, pz, r, an, sc)
            assert actual == expected, (
                f"Hash mismatch for input '{sp}|{pz}|{r}|{an or ''}|{sc}':\n"
                f"  Python:     {actual}\n"
                f"  TypeScript: {expected}"
            )
